<script>
    $(function(){
        var obj = "";
        $("#sele").change(function(){
            obj = $(this);
            var id = obj.val();
            var str = "";
            if(id==0){
                str += '<select  id="names">';
                str += '<option option value="0">'+ '请选择……'+'</option>'
                str += '</select>'
                $("#span").html(str);
            }else{
                $.ajax({
                    type: "GET",
                    url: "/select",
                    dataType:'json',
                    data: {
                        id:id
                    },
                    success: function(msg){
                        if(msg==0){
                            alert('暂无此品牌车辆')
                            str += '<select  id="names">';
                            str += '<option option value="0">'+ '请选择……'+'</option>'
                            str += '</select>'
                            $("#span").html(str);
                        }else{
                            str += '<select  id="names">';
                            $.each( msg, function(i, n){
                                str += '<option value="'+ n.brand_id +'">'+ n.brand_name +'&nbsp;&nbsp;&nbsp;&nbsp;</option>';
                            });
                            str += '</select>';
                            $("#span").html(str);
                        }
                    }
                });
            }
        })

    })
</script>
